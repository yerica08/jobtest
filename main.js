function updateProgress() {
  if (currentStep === 0) {
    progressText.textContent = " ";
    progressFill.style.width = "0%";
    return;
  }
  const percent = (currentStep / totalQuestions) * 100;
  progressText.textContent = `Q${currentStep} / ${totalQuestions}`;
  progressFill.style.width = percent + "%";
}

function showScreen(step) {
  currentStep = step;
  screenIntro.classList.remove("active");
  screenResult.classList.remove("active");
  questionScreens.forEach(s => s.classList.remove("active"));

  if (step === 0) {
    screenIntro.classList.add("active");
  } else if (step >= 1 && step <= totalQuestions) {
    const target = questionScreens.find(s => Number(s.dataset.step) === step);
    if (target) target.classList.add("active");
    // ì„ íƒí–ˆë˜ ì˜µì…˜ í‘œì‹œ
    restoreSelection(target);
  } else if (step === totalQuestions + 1) {
    screenResult.classList.add("active");
  }
  updateProgress();
}

function restoreSelection(screen) {
  if (!screen) return;
  const qid = screen.dataset.qid;
  const chosen = answers[qid];
  const optionBtns = screen.querySelectorAll(".option-btn");
  optionBtns.forEach(btn => {
    btn.classList.toggle("selected", btn.dataset.value === chosen);
  });
  const err = screen.querySelector(".error-msg");
  if (err) err.textContent = "";
}

